package com.example.demo.model;

public class Historique {

	int idenchere;
	
	String nommaterielle;
	
	int qte;
	
	int prixminimal;
	
	String nomclient;
	
	int montantpayer;

	public Historique() {
		super();
	}

	public int getIdenchere() {
		return idenchere;
	}

	public void setIdenchere(int idenchere) {
		this.idenchere = idenchere;
	}

	public String getNommaterielle() {
		return nommaterielle;
	}

	public void setNommaterielle(String nommaterielle) {
		this.nommaterielle = nommaterielle;
	}

	public int getQte() {
		return qte;
	}

	public void setQte(int qte) {
		this.qte = qte;
	}

	public int getPrixminimal() {
		return prixminimal;
	}

	public void setPrixminimal(int prixminimal) {
		this.prixminimal = prixminimal;
	}

	public String getNomclient() {
		return nomclient;
	}

	public void setNomclient(String nomclient) {
		this.nomclient = nomclient;
	}

	public int getMontantpayer() {
		return montantpayer;
	}

	public void setMontantpayer(int montantpayer) {
		this.montantpayer = montantpayer;
	}
	
	
	
}

package com.example.demo.model;

public class State {

	int max;
	
	int min;
	
	float avg;
	
	float sqrt;

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public float getAvg() {
		return avg;
	}

	public void setAvg(float avg) {
		this.avg = avg;
	}

	public float getSqrt() {
		return sqrt;
	}

	public void setSqrt(float sqrt) {
		this.sqrt = sqrt;
	}

	public State() {
		super();
	}
	
	
	
}
